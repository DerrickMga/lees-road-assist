@app.post("/webhook")
async def receive_webhook(request: Request):
    """
    Receive incoming WhatsApp messages from Safenet / Meta Cloud API.
    Validates signature if APP_SECRET is configured, then processes messages.
    """
    raw_body = await request.body()

    # Optional: validate Meta HMAC-SHA256 signature
    if APP_SECRET:
        sig_header = request.headers.get("x-hub-signature-256", "")
        expected = "sha256=" + hmac.new(
            APP_SECRET.encode(), raw_body, hashlib.sha256
        ).hexdigest()
        if not hmac.compare_digest(sig_header, expected):
            logger.warning("Signature validation failed")
            raise HTTPException(status_code=status.HTTP_401_UNAUTHORIZED, detail="Invalid signature")

    try:
        body = json.loads(raw_body)
    except json.JSONDecodeError:
        raise HTTPException(status_code=status.HTTP_400_BAD_REQUEST, detail="Invalid JSON")

    logger.debug(f"Incoming payload: {json.dumps(body)[:500]}")

    # Meta / Safenet Cloud API format
    for entry in body.get("entry", []):
        for change in entry.get("changes", []):
            value = change.get("value", {})

            # Handle delivery/read status updates (no reply needed)
            for s in value.get("statuses", []):
                logger.info(f"Status update: msg_id={s.get('id')} → {s.get('status')}")

            # Handle incoming messages
            for msg in value.get("messages", []):
                phone: str = msg.get("from", "")
                msg_type: str = msg.get("type", "text")
                msg_id: str = msg.get("id", "")

                # Extract text content based on type
                content = ""
                if msg_type == "text":
                    content = msg.get("text", {}).get("body", "")
                elif msg_type == "interactive":
                    interactive = msg.get("interactive", {})
                    reply = interactive.get("button_reply") or interactive.get("list_reply") or {}
                    content = reply.get("id", "") or reply.get("title", "")
                elif msg_type == "location":
                    loc = msg.get("location", {})
                    content = f"GPS:{loc.get('latitude')},{loc.get('longitude')}"

                logger.info(f"📨 from={phone} type={msg_type} content='{content[:80]}'")

                if phone:
                    await handle_message(phone, content, msg_type, msg)

    return {"status": "ok"}


# ── Flow endpoint encryption helpers ─────────────────────────────────────────

def _decrypt_flow_request(body: dict) -> dict:
    """
    Decrypt an incoming encrypted WABA Flow request.
    Meta encrypts all requests with the public key registered via
    POST /{phone_number_id}/whatsapp_business_encryption.

    Body schema: {encrypted_flow_data, encrypted_aes_key, initial_vector}
    Returns decrypted payload dict.
    """
    if not _CRYPTO_AVAILABLE or not _FLOW_PRIVATE_KEY:
        raise HTTPException(status_code=503, detail="Crypto not available — cannot decrypt flow request")

    try:
        encrypted_aes_key  = base64.b64decode(body["encrypted_aes_key"])
        initial_vector     = base64.b64decode(body["initial_vector"])
        encrypted_flow_data = base64.b64decode(body["encrypted_flow_data"])
    except (KeyError, Exception) as e:
        raise HTTPException(status_code=400, detail=f"Bad encrypted flow body: {e}")

    # Decrypt the AES-256 key using our RSA private key (OAEP + SHA-256)
    try:
        aes_key = _FLOW_PRIVATE_KEY.decrypt(
            encrypted_aes_key,
            _asym_padding.OAEP(
                mgf=_asym_padding.MGF1(algorithm=_hashes.SHA256()),
                algorithm=_hashes.SHA256(),
                label=None,
            ),
        )
    except Exception as e:
        raise HTTPException(status_code=400, detail=f"AES key decryption failed: {e}")

    # Decrypt the flow data with AES-256-GCM
    try:
        decrypted_bytes = AESGCM(aes_key).decrypt(initial_vector, encrypted_flow_data, None)
        return json.loads(decrypted_bytes), aes_key, initial_vector
    except Exception as e:
        raise HTTPException(status_code=400, detail=f"AES-GCM decryption failed: {e}")


def _encrypt_flow_response(response_payload: dict, aes_key: bytes, initial_vector: bytes) -> str:
    """
    Encrypt the response payload using AES-256-GCM.
    Meta requires the IV to be bit-flipped (XOR 0xFF) for the response.
    Returns base64-encoded ciphertext.
    """
    flipped_iv = bytes(b ^ 0xFF for b in initial_vector)
    ciphertext = AESGCM(aes_key).encrypt(flipped_iv, json.dumps(response_payload).encode(), None)
    return base64.b64encode(ciphertext).decode()


@app.post("/flow-endpoint")
async def flow_data_exchange(request: Request):
    """
    WhatsApp Flows data exchange endpoint (E2E encrypted).

    Meta encrypts every POST to this URL (including health pings).
    We decrypt, process, then encrypt and return the response.

    Docs: https://developers.facebook.com/docs/whatsapp/flows/reference/flowdataexchangeendpoint
    """
    try:
        body = await request.json()
    except Exception:
        raise HTTPException(status_code=400, detail="Invalid JSON body")

    # Unencrypted health check used by Meta's pre-registration check
    if body.get("action") == "ping" and "encrypted_flow_data" not in body:
        return {"version": "3.0", "data": {"status": "active"}}

    # All normal requests are encrypted
    decrypted, aes_key, iv = _decrypt_flow_request(body)

    action     = decrypted.get("action", "")
    screen     = decrypted.get("screen", "")
    data       = decrypted.get("data", {})
    flow_token = decrypted.get("flow_token", "")

    logger.info(
        "Flow data exchange: action=%r screen=%r token=%s...",
        action, screen, str(flow_token)[:12]
    )

    # Encrypted ping / health check
    if action == "ping":
        resp_payload = {"version": "3.0", "data": {"status": "active"}}
        return Response(
            content=json.dumps({"response": _encrypt_flow_response(resp_payload, aes_key, iv)}),
            media_type="application/json",
        )

    # INIT — return empty data (our screens are static)
    if action == "INIT":
        resp_payload = {"version": "3.0", "screen": screen or "SERVICE_SELECT", "data": {}}
        return Response(
            content=json.dumps({"response": _encrypt_flow_response(resp_payload, aes_key, iv)}),
            media_type="application/json",
        )

    # Pass data through for any navigate / BACK actions
    resp_payload = {"version": "3.0", "data": data}
    return Response(
        content=json.dumps({"response": _encrypt_flow_response(resp_payload, aes_key, iv)}),
        media_type="application/json",
    )


