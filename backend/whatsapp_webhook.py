async def handle_message(phone: str, text: str, msg_type: str, raw_msg: dict) -> None:
    """
    Drive the conversation state machine.
    Each function corresponds to a flow stage.
    """
    session = get_session(phone)
    flow = session["flow"]

    # ── WABA Flow completion (nfm_reply) ──────────────────────────────────────
    # This fires when the user submits a WhatsApp Flow form.
    if msg_type == "interactive":
        interactive = raw_msg.get("interactive", {})
        if interactive.get("type") == "nfm_reply":
            nfm = interactive.get("nfm_reply", {})
            try:
                flow_data = json.loads(nfm.get("response_json", "{}"))
            except json.JSONDecodeError:
                flow_data = {}
            logger.info(f"[{phone}] nfm_reply received: {json.dumps(flow_data)[:200]}")
            # Route based on current session flow
            if flow in ("flow_provider_pending",) or flow_data.get("assignment_id"):
                await handle_provider_flow_reply(phone, flow_data)
            else:
                await handle_customer_flow_reply(phone, flow_data)
            return

    # Make input handling case-insensitive; trim whitespace
    text_lower = (text or "").strip().lower()

    logger.info(f"[{phone}] flow={flow} | msg='{text[:60] if text else '<empty>'}'")

    # Global escape: "menu", "hi", "hello", "restart" always restarts
    if text_lower in ("menu", "hi", "hello", "restart", "start", "0") and flow not in ("greeting",):
        reset_session(phone)
        flow = "greeting"

    # ── GREETING ──────────────────────────────────────────────────────────────
    if flow == "greeting":
        if CUSTOMER_FLOW_ID:
            # Send the interactive Flow (native WhatsApp form UI)
            set_session(phone, "flow_customer_pending")
            await wa_text(phone,
                "👋 Welcome to *Lee's Road Assist* — Zimbabwe's #1 roadside assistance service!\n\n"
                "We're available *24/7* across Harare, Bulawayo, and major routes.\n\n"
                "Tap the button below to request help ⤵️"
            )
            await wa_flow(
                phone,
                flow_id=CUSTOMER_FLOW_ID,
                flow_cta="Request Assistance",
                header="Lee's Road Assist",
                body="Fill in the short form and we'll dispatch the nearest certified technician to you.",
                init_screen="SERVICE_SELECT",
            )
        else:
            # Fallback: text-based conversation (no Flow ID configured yet)
            set_session(phone, "choose_service")
            await wa_text(phone,
                "👋 Welcome to *Lee's Road Assist* — Zimbabwe's #1 roadside assistance service!\n\n"
                "We're available *24/7* across Harare, Bulawayo, and major routes.\n\n"
                "How can we help you today?"
            )
            await wa_interactive_list(
                phone,
                header="Lee's Road Assist",
                body="Select the service you need and we'll dispatch a technician to your location.",
                button="View Services",
                sections=SERVICE_SECTIONS,
            )
        return

    # ── CHOOSE SERVICE ────────────────────────────────────────────────────────
    if flow == "choose_service":
        # Interactive list reply comes as interactive.list_reply.id
        service_id = None
        if msg_type == "interactive":
            interactive = raw_msg.get("interactive", {})
            reply = interactive.get("list_reply") or interactive.get("button_reply") or {}
            service_id = reply.get("id")
        elif text_lower in SERVICE_LABELS:
            service_id = text_lower

        if not service_id or service_id not in SERVICE_LABELS:
            await wa_text(phone,
                "Please tap *View Services* above to choose a service, or type *menu* to start over."
            )
            return

        update_data(phone, service=service_id, service_label=SERVICE_LABELS[service_id])
        set_session(phone, "collect_location", _sessions[phone]["data"])

        await wa_text(phone,
            f"Got it — *{SERVICE_LABELS[service_id]}* 👍\n\n"
            "📍 Please share your *location*:\n"
            "• Tap the 📎 attachment icon → *Location* to share GPS coordinates, OR\n"
            "• Type your address/area (e.g. _Avondale, Harare_ or _Corner 1st & Main_)"
        )
        return

    # ── COLLECT LOCATION ──────────────────────────────────────────────────────
    if flow == "collect_location":
        location_text = None

        if msg_type == "location":
            loc = raw_msg.get("location", {})
            lat = loc.get("latitude")
            lng = loc.get("longitude")
            name = loc.get("name", "")
            address = loc.get("address", "")
            location_text = f"{name or ''} {address or ''} (GPS: {lat},{lng})".strip()
        elif text and len(text.strip()) >= 5:
            location_text = text.strip()

        if not location_text:
            await wa_text(phone,
                "We need your location to dispatch a technician.\n"
                "Please share your GPS via the attachment icon, or type your area/address."
            )
            return

        update_data(phone, location=location_text)
        set_session(phone, "collect_description", _sessions[phone]["data"])

        await wa_interactive_buttons(
            phone,
            body=(
                f"📍 Location captured: _{location_text}_\n\n"
                "Is there anything specific we should know? (vehicle make/model, problem details)\n\n"
                "Or tap *Skip* to proceed straight to confirmation."
            ),
            buttons=[("skip_desc", "Skip"), ("add_desc", "Add Details")],
        )
        return

    # ── COLLECT DESCRIPTION (optional) ───────────────────────────────────────
    if flow == "collect_description":
        skip = False
        description = None

        if msg_type == "interactive":
            interactive = raw_msg.get("interactive", {})
            reply = interactive.get("button_reply") or {}
            if reply.get("id") == "skip_desc":
                skip = True
            elif reply.get("id") == "add_desc":
                set_session(phone, "collect_description_text", _sessions[phone]["data"])
                await wa_text(phone,
                    "Please type your vehicle/issue details (e.g. _Toyota Corolla 2018, won't start_):"
                )
                return
        elif text:
            description = text.strip()

        if not skip and not description:
            # They tapped "Add Details" last time — now collecting the text
            description = text.strip() if text else None

        update_data(phone, description=description or "")
        set_session(phone, "confirm", _sessions[phone]["data"])
        await send_confirmation_prompt(phone)
        return

    # ── COLLECT DESCRIPTION TEXT ──────────────────────────────────────────────
    if flow == "collect_description_text":
        description = text.strip() if text else ""
        update_data(phone, description=description)
        set_session(phone, "confirm", _sessions[phone]["data"])
        await send_confirmation_prompt(phone)
        return

    # ── CONFIRM ───────────────────────────────────────────────────────────────
    if flow == "confirm":
        reply_id = None
        if msg_type == "interactive":
            interactive = raw_msg.get("interactive", {})
            reply = interactive.get("button_reply") or {}
            reply_id = reply.get("id")
        elif text_lower in ("yes", "confirm", "ok", "yep", "sure"):
            reply_id = "confirm_yes"
        elif text_lower in ("no", "cancel", "back"):
            reply_id = "confirm_no"

        if reply_id == "confirm_yes":
            await create_request_and_confirm(phone)
        elif reply_id == "confirm_no":
            reset_session(phone)
            set_session(phone, "choose_service")
            await wa_interactive_list(
                phone,
                header="Lee's Road Assist",
                body="No problem! What service do you need?",
                button="View Services",
                sections=SERVICE_SECTIONS,
            )
        else:
            await send_confirmation_prompt(phone)
        return

    # ── CHOOSE PAYMENT METHOD ─────────────────────────────────────────────────
    if flow == "choose_payment":
        reply_id = None
        if msg_type == "interactive":
            interactive = raw_msg.get("interactive", {})
            reply = interactive.get("list_reply") or interactive.get("button_reply") or {}
            reply_id = reply.get("id")
        elif text_lower in ("cash", "ecocash", "innbucks", "onemoney"):
            reply_id = text_lower

        if reply_id in ("pay_cash", "cash"):
            update_data(phone, payment_method="cash")
            await finalize_payment(phone, "cash")
        elif reply_id in ("pay_ecocash", "ecocash"):
            update_data(phone, payment_method="ecocash")
            set_session(phone, "collect_payment_phone", get_session(phone)["data"])
            await wa_text(phone,
                "📱 *EcoCash Payment*\n\n"
                "Please enter your EcoCash mobile number:\n"
                "(e.g. 0771 234 567 or +263771234567)"
            )
        elif reply_id in ("pay_innbucks", "innbucks"):
            update_data(phone, payment_method="innbucks")
            set_session(phone, "collect_payment_phone", get_session(phone)["data"])
            await wa_text(phone,
                "📱 *InnBucks Payment*\n\n"
                "Please enter your InnBucks mobile number:\n"
                "(e.g. 0771 234 567 or +263771234567)"
            )
        elif reply_id in ("pay_onemoney", "onemoney"):
            update_data(phone, payment_method="onemoney")
            set_session(phone, "collect_payment_phone", get_session(phone)["data"])
            await wa_text(phone,
                "📱 *OneMoney Payment*\n\n"
                "Please enter your OneMoney mobile number:\n"
                "(e.g. 0771 234 567 or +263771234567)"
            )
        else:
            await send_payment_choice_prompt(phone)
        return

    # ── COLLECT PAYMENT PHONE ─────────────────────────────────────────────────
    if flow == "collect_payment_phone":
        if not text or len(text.strip()) < 7:
            await wa_text(phone, "Please enter a valid mobile number (at least 7 digits).")
            return
        update_data(phone, payment_phone=text.strip())
        await finalize_payment(phone, get_session(phone)["data"].get("payment_method", "cash"))
        return

    # ── WAITING / ACTIVE REQUEST ──────────────────────────────────────────────
    if flow in ("waiting", "active"):
        await wa_text(phone,
            "Your request is already active and a technician has been assigned. 🚘\n\n"
            "Type *menu* to start a new request or call *+263 8612 166749* for urgent assistance."
        )
        return
    reset_session(phone)
    await wa_text(phone, "Type *hi* or *menu* to start a new request. 👋")


async def send_confirmation_prompt(phone: str) -> None:
    data = get_session(phone)["data"]
    service_label = data.get("service_label", "Unknown")
    service_id    = data.get("service", "other")
    location      = data.get("location", "Not provided")
    description   = data.get("description", "")

    BACKEND_SERVICE_MAP = {
        "battery":   "battery_jumpstart",
        "tyre":      "tyre_change",
        "fuel":      "fuel_delivery",
        "lockout":   "lockout_rescue",
        "repair":    "onsite_repair",
        "tow_local": "towing",
        "tow_long":  "towing",
        "accident":  "accident_assistance",
        "other":     "other",
    }
    service_slug = BACKEND_SERVICE_MAP.get(service_id, "other")

    # Fetch live price estimate
    price_line = ""
    try:
        async with httpx.AsyncClient(timeout=10) as client:
            headers = {"Content-Type": "application/json"}
            if BACKEND_API_KEY:
                headers["X-Internal-Key"] = BACKEND_API_KEY
            pr = await client.post(
                f"{BACKEND_API}/services/pricing/estimate",
                headers=headers,
                json={
                    "service_type_code": service_slug,
                    "pickup_latitude": -17.8292,
                    "pickup_longitude": 31.0522,
                },
            )
            if pr.status_code == 200:
                p = pr.json()
                price_line = (
                    f"\n💰 *Estimated Cost*\n"
                    f"   🔧 Service fee: ${p.get('service_fee', 5):.2f}\n"
                    f"   📍 Callout fee: ${p.get('callout_fee', 5):.2f}\n"
                )
                if p.get("towing_cost", 0) > 0:
                    price_line += f"   🚛 Towing: ${p.get('towing_cost'):.2f}\n"
                price_line += f"   💵 *Total: ${p.get('total'):.2f} USD*\n"
    except Exception:
        pass

    summary = (
        f"📋 *Request Summary*\n\n"
        f"🔧 Service: *{service_label}*\n"
        f"📍 Location: {location}\n"
    )
    if description:
        summary += f"📝 Details: {description}\n"
    if price_line:
        summary += price_line
    summary += "\nShall we dispatch a technician to you now?\n_(You'll choose payment method next)_"

    await wa_interactive_buttons(
        phone,
        body=summary,
        buttons=[("confirm_yes", "✅ Yes, dispatch!"), ("confirm_no", "❌ Cancel")],
    )


async def create_request_and_confirm(phone: str) -> None:
    """Create service request via backend API, then route to payment step."""
    data = get_session(phone)["data"]
    service_id     = data.get("service")
    service_label  = data.get("service_label", "Roadside Assistance")
    location       = data.get("location", "")
    description    = data.get("description", "")
    payment_method = data.get("payment_method", "cash").lower()

    request_uuid = None
    request_id   = None
    estimated_total: float | None = None
    request_error = None

    # Map our service IDs to backend service type codes
    BACKEND_SERVICE_MAP = {
        "battery":   "battery_jumpstart",
        "tyre":      "tyre_change",
        "fuel":      "fuel_delivery",
        "lockout":   "lockout_rescue",
        "repair":    "onsite_repair",
        "tow_local": "towing",
        "tow_long":  "towing",
        "accident":  "accident_assistance",
        "other":     "other",
    }

    try:
        async with httpx.AsyncClient(timeout=15) as client:
            headers = {"Content-Type": "application/json"}
            if BACKEND_API_KEY:
                headers["X-Internal-Key"] = BACKEND_API_KEY

            resp = await client.post(
                f"{BACKEND_API}/requests/whatsapp",
                headers=headers,
                json={
                    "phone": phone,
                    "service_slug": BACKEND_SERVICE_MAP.get(service_id, "other"),
                    "pickup_address": location,
                    "issue_description": f"{description} [via WhatsApp]".strip(),
                    "channel": "whatsapp",
                    "payment_method": payment_method,
                },
            )
            if resp.status_code in (200, 201):
                rdata = resp.json()
                request_uuid = rdata.get("uuid") or rdata.get("id")
                request_id   = rdata.get("id")
                estimated_total = rdata.get("estimated_price")
            else:
                request_error = resp.text[:200]
                logger.error(f"Backend request creation failed {resp.status_code}: {request_error}")
    except Exception as e:
        request_error = str(e)
        logger.error(f"Backend API error: {e}")

    if request_uuid:
        # Store request details and route to payment
        update_data(phone,
            request_uuid=str(request_uuid),
            request_id=str(request_id) if request_id else None,
            estimated_total=float(estimated_total) if estimated_total else None,
        )
        set_session(phone, "choose_payment", get_session(phone)["data"])
