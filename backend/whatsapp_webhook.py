# ─── Utility: send a WhatsApp message ─────────────────────────────────────────

async def wa_send(to: str, payload: dict) -> dict:
    """POST a message payload to Meta Graph API."""
    url = f"{GRAPH_URL}/{PHONE_NUMBER_ID}/messages"
    async with httpx.AsyncClient(timeout=15) as client:
        resp = await client.post(
            url,
            headers={
                "Authorization": f"Bearer {ACCESS_TOKEN}",
                "Content-Type": "application/json",
            },
            json={"messaging_product": "whatsapp", "to": to, **payload},
        )
        if resp.status_code >= 400:
            logger.error(f"WA send error {resp.status_code}: {resp.text[:300]}")
        return resp.json()


async def wa_text(to: str, body: str) -> None:
    await wa_send(to, {"type": "text", "text": {"body": body, "preview_url": False}})


async def wa_interactive_list(to: str, header: str, body: str, button: str, sections: list) -> None:
    await wa_send(to, {
        "type": "interactive",
        "interactive": {
            "type": "list",
            "header": {"type": "text", "text": header},
            "body": {"text": body},
            "action": {"button": button, "sections": sections},
        },
    })


async def wa_interactive_buttons(to: str, body: str, buttons: list[tuple[str, str]]) -> None:
    """buttons: list of (id, label) tuples — max 3."""
    await wa_send(to, {
        "type": "interactive",
        "interactive": {
            "type": "button",
            "body": {"text": body},
            "action": {
                "buttons": [
                    {"type": "reply", "reply": {"id": bid, "title": label}}
                    for bid, label in buttons[:3]
                ]
            },
        },
    })


async def wa_flow(
    to: str,
    flow_id: str,
    flow_cta: str,
    header: str,
    body: str,
    init_screen: str = "SERVICE_SELECT",
    flow_data: Optional[dict] = None,
    footer: str = "Lee's Road Assist — 24/7",
) -> str:
    """
    Send a WhatsApp Flow message.
    Returns the flow_token (UUID) so it can be stored for correlation.
    Requires ACCESS_TOKEN and PHONE_NUMBER_ID to be set.
    """
    token = str(uuid.uuid4())
    action_payload: dict = {"screen": init_screen}
    if flow_data:
        action_payload["data"] = flow_data
    params: dict = {
        "flow_message_version": "3",
        "flow_token": token,
        "flow_id": flow_id,
        "flow_cta": flow_cta,
        "flow_action": "navigate",
        "flow_action_payload": action_payload,
    }
    # v6.1 flows use only navigate/complete — no endpoint needed.
    # Do NOT include flow_endpoint; it would trigger Meta's health check.
    await wa_send(to, {
        "type": "interactive",
        "interactive": {
            "type": "flow",
            "header": {"type": "text", "text": header},
            "body": {"text": body},
            "footer": {"text": footer},
            "action": {
                "name": "flow",
                "parameters": params,
            },
        },
    })
    return token


# ─── Session helpers ──────────────────────────────────────────────────────────

def get_session(phone: str) -> dict:
    return _sessions.get(phone, {"flow": "greeting", "data": {}})


def set_session(phone: str, flow: str, data: Optional[dict] = None) -> None:
    _sessions[phone] = {
        "flow": flow,
        "data": data or _sessions.get(phone, {}).get("data", {}),
        "updated_at": datetime.now(timezone.utc).isoformat(),
    }


def update_data(phone: str, **kwargs) -> None:
    session = get_session(phone)
    session["data"].update(kwargs)
    _sessions[phone] = session


def reset_session(phone: str) -> None:
    _sessions.pop(phone, None)


